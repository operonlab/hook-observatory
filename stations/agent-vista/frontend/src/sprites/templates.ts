// ── Exported animation sheet ─────────────────

export type Direction = 'down' | 'up' | 'right' | 'left';
export type AnimKey = 'IDLE' | 'WALK' | 'TYPE' | 'THINK' | 'WAIT' | 'ERROR';

export interface SpriteSheet {
  frames: SpriteFrame[];
  fps: number;
}

// Direction-specific idle/walk sheets
const dirSheets: Record<'down' | 'up' | 'right', Record<'IDLE' | 'WALK', SpriteSheet>> = {
  down: {
    IDLE: { frames: [downIdle0, downIdle1], fps: 2 },
    WALK: { frames: [downWalk0, downWalk1], fps: 4 },
  },
  up: {
    IDLE: { frames: [upIdle0, upIdle1], fps: 2 },
    WALK: { frames: [upWalk0, upWalk1], fps: 4 },
  },
  right: {
    IDLE: { frames: [rightIdle0, rightIdle1], fps: 2 },
    WALK: { frames: [rightWalk0, rightWalk1], fps: 4 },
  },
};

// State sheets (direction-independent for seated states)
const stateSheets: Record<'TYPE' | 'THINK' | 'WAIT' | 'ERROR', SpriteSheet> = {
  TYPE:  { frames: [typeFrame0, typeFrame1], fps: 4 },
  THINK: { frames: [thinkFrame, thinkFrame], fps: 1 },
  WAIT:  { frames: [waitFrame, waitFrame], fps: 1 },
  ERROR: { frames: [errorFrame, errorFrame], fps: 2 },
};

/** Get the sprite sheet for a given animation state and direction. */
export function getSpriteSheet(anim: AnimKey, dir: Direction): SpriteSheet {
  // For seated states: use state-specific sprite only when facing front (down),
  // otherwise show directional idle to preserve facing direction
  if (anim in stateSheets) {
    if (dir === 'down') return stateSheets[anim as keyof typeof stateSheets];
    const d = dir === 'left' ? 'right' : dir;
    return dirSheets[d].IDLE;
  }

  // Left = mirrored right (handled at render time)
  const d = dir === 'left' ? 'right' : dir;
  return dirSheets[d][anim as 'IDLE' | 'WALK'];
}

