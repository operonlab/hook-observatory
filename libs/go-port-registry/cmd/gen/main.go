// Command gen reads shared/schemas/port_registry.yaml and writes ports.go.
//
// It runs via `go generate ./...` from libs/go-port-registry. The output
// (ports.go) is checked in so downstream consumers don't need the yaml
// parser at compile time.
//
// To regenerate after editing yaml:
//
//	cd libs/go-port-registry && go generate ./...
//
// The yaml path is resolved relative to this file: ../../../shared/schemas
// (libs/go-port-registry/cmd/gen → workshop root → shared/schemas).
package main

import (
	"fmt"
	"os"
	"path/filepath"
	"runtime"
	"strings"

	"gopkg.in/yaml.v3"
)

type registry struct {
	Host     string `yaml:"host"`
	Services []svc  `yaml:"services"`
}

type svc struct {
	Name       string `yaml:"name"`
	Port       uint16 `yaml:"port"`
	Group      string `yaml:"group"`
	HealthPath string `yaml:"health_path"`
	EnvVar     string `yaml:"env_var"`
	NginxPath  string `yaml:"nginx_path"`
	Optional   bool   `yaml:"optional"`
}

func main() {
	// Locate this source file so the tool works no matter the cwd.
	_, thisFile, _, ok := runtime.Caller(0)
	if !ok {
		die("cannot determine gen source path")
	}
	// thisFile = …/libs/go-port-registry/cmd/gen/main.go
	pkgDir := filepath.Dir(filepath.Dir(filepath.Dir(thisFile))) // …/libs/go-port-registry
	repoRoot := filepath.Dir(filepath.Dir(pkgDir))               // workshop root
	yamlPath := filepath.Join(repoRoot, "shared", "schemas", "port_registry.yaml")
	outPath := filepath.Join(pkgDir, "ports.go")

	// Allow override for tests / out-of-tree builds.
	if v := os.Getenv("PORT_REGISTRY_YAML"); v != "" {
		yamlPath = v
	}
	if v := os.Getenv("PORT_REGISTRY_OUT"); v != "" {
		outPath = v
	}

	raw, err := os.ReadFile(yamlPath)
	if err != nil {
		die("read %s: %v", yamlPath, err)
	}

	var reg registry
	if err := yaml.Unmarshal(raw, &reg); err != nil {
		die("parse %s: %v", yamlPath, err)
	}
	if reg.Host == "" {
		reg.Host = "127.0.0.1"
	}

	out := render(&reg, yamlPath)
	if err := os.WriteFile(outPath, []byte(out), 0o644); err != nil {
		die("write %s: %v", outPath, err)
	}
	fmt.Printf("port-registry: wrote %d services to %s\n", len(reg.Services), outPath)
}

func render(reg *registry, yamlPath string) string {
	var b strings.Builder
	b.WriteString("// Code generated by libs/go-port-registry/cmd/gen — DO NOT EDIT.\n")
	b.WriteString("// Source: shared/schemas/port_registry.yaml\n")
	b.WriteString("// Run `go generate ./...` from libs/go-port-registry to regenerate.\n\n")
	b.WriteString("package portregistry\n\n")
	fmt.Fprintf(&b, "// Host is the bind address shared by every Workshop-managed service.\n")
	fmt.Fprintf(&b, "const Host = %q\n\n", reg.Host)
	fmt.Fprintf(&b, "// Ports lists every service in registry order.\n")
	fmt.Fprintf(&b, "var Ports = []ServicePort{\n")
	for _, s := range reg.Services {
		fmt.Fprintf(
			&b,
			"\t{Name: %q, Port: %d, Group: %q, HealthPath: %q, EnvVar: %q, NginxPath: %q, Optional: %t},\n",
			s.Name, s.Port, s.Group, s.HealthPath, s.EnvVar, s.NginxPath, s.Optional,
		)
	}
	b.WriteString("}\n")
	_ = yamlPath // reserved for future provenance comment if useful
	return b.String()
}

func die(format string, args ...any) {
	fmt.Fprintf(os.Stderr, "port-registry gen: "+format+"\n", args...)
	os.Exit(1)
}
